// Create Math functions that will console log the result

// each function should take in 2 input, an 'x' and an 'y'
// at the end of each function, output the result using console log

function add(x,y){
console.log(x+y);
}

function minus(x,y){
console.log(x-y);
}

function multiply(x,y){
console.log(x*y);
}

function divide(x,y){
console.log(x/y);
}

